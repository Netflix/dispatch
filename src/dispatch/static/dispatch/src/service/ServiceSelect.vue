<template>
  <ValidationProvider name="service" immediate>
    <v-autocomplete
      v-model="service"
      :items="items"
      :search-input.sync="search"
      :menu-props="{ maxHeight: '400' }"
      item-text="name"
      :label="label"
      placeholder="Start typing to search"
      return-object
      :hint="hint"
      :loading="loading"
      no-filter
    >
      <template slot="append-outer">
        <v-btn icon @click="createEditShow({})"><v-icon>add</v-icon></v-btn>
        <new-edit-sheet @new-service-created="addItem($event)" />
      </template>
    </v-autocomplete>
  </ValidationProvider>
</template>

<script>
import { mapActions } from "vuex"
import { cloneDeep } from "lodash"
import { ValidationProvider } from "vee-validate"

import ServiceApi from "@/service/api"
import NewEditSheet from "@/service/NewEditSheet.vue"

export default {
  name: "ServiceSelect",

  props: {
    value: {
      type: Object,
      default: function() {
        return {}
      }
    },
    label: {
      type: String,
      default: function() {
        return "Service"
      }
    },
    hint: {
      type: String,
      default: function() {
        return "Service to associate"
      }
    }
  },

  components: {
    ValidationProvider,
    NewEditSheet
  },

  data() {
    return {
      loading: false,
      search: null,
      select: null,
      items: []
    }
  },

  watch: {
    search(val) {
      val && val !== this.select && this.querySelections(val)
    },
    value(val) {
      if (!val) return
      this.items.push(val)
    }
  },

  computed: {
    service: {
      get() {
        return cloneDeep(this.value)
      },
      set(value) {
        this.$emit("input", value)
      }
    }
  },

  methods: {
    ...mapActions("service", ["createEditShow"]),
    addItem(v) {
      this.service = v
      this.items.push(v)
    },
    querySelections(v) {
      this.loading = "error"
      // Simulated ajax query
      ServiceApi.getAll({ q: v }).then(response => {
        this.items = response.data.items
        this.loading = false
      })
    }
  },

  mounted() {
    this.error = null
    this.loading = "error"
    ServiceApi.getAll().then(response => {
      this.items = response.data.items
      this.loading = false
    })
  }
}
</script>
